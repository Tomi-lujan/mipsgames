# Car Dodge Game - Sprite ROJO completo + movimiento adelante/atr?s
# SETUP:
#    Tools -> Bitmap Display:
#    Unit Width: 4, Unit Height: 4
#    Display Width: 256, Display Height: 512
#    Base: 0x10008000 ($gp)
#    Connect to MIPS
# 3. Tools -> Keyboard MMIO -> Connect to MIPS
# 4. Run -> Assemble (F3)
# 5. Run -> Go (F5)
#
# CONTROLES:
# A - Izquierda
# D - Derecha  
# W - Adelante (acelerar)
# S - Atr?s (frenar)
# Q - Salir

.data
    display: .word 0x10008000
   
    # Colores del auto ROJO (tu sprite)
    colorBlack: .word 0x000000
    colorRed1: .word 0xFF0000      # Rojo brillante del cuerpo
    colorRed2: .word 0xB40000      # Rojo medio
    colorRed3: .word 0x8B0000      # Rojo oscuro
    colorGray1: .word 0x464646     # Gris ruedas
    colorGray2: .word 0x2E2E2E     # Gris oscuro
    colorBlue: .word 0x0000FF      # Azul para ventanas/detalles
    colorWhite: .word 0xFFFFFF
   
    carX: .word 28        # X del auto (centrado)
    carLane: .word 1      # 0=izq, 1=centro, 2=derecha
    carY: .word 50        # Y del auto
    carSpeed: .word 0     # Velocidad del auto (0-3)
   
    obsX: .word 28        # X del obst?culo
    obsY: .word 0         # Y inicial
    obsSpeed: .word 1     # Velocidad base del obst?culo
   
    score: .word 0
    lives: .word 3
   
    minX: .word 4
    maxX: .word 56
    minY: .word 10
    maxY: .word 58
   
    lane0X: .word 13      # X del carril izquierdo (m?s a la izquierda)
    lane1X: .word 32      # X del carril centro (centrado en pantalla)
    lane2X: .word 51      # X del carril derecho (m?s a la derecha)
   
    obsLane0X: .word 5     # Carril izq: 13 (centro auto) - 8 (mitad cono) = 5
    obsLane1X: .word 24    # Carril centro: 32 - 8 = 24
    obsLane2X: .word 43    # Carril derecho: 51 - 8 = 43

.text
main:
    li $s0, 0x10008000
   
    # Dibujar fondo gris una sola vez
    li $a0, 0x10008000
    li $a1, 0x5d5d5d
    jal fillScreen
   
    # Dibujar bordes azules
    li $t0, 0
borderLoop:
    li $a0, 0
    move $a1, $t0
    li $a2, 0x0000DA
    jal setPixel
    li $a0, 1
    jal setPixel
    li $a0, 62
    jal setPixel
    li $a0, 63
    jal setPixel
    addi $t0, $t0, 1
    li $t1, 128
    blt $t0, $t1, borderLoop
    li $s0, 0x10008000
   
    # Inicializar carril en centro (carril 1)
    li $t0, 1
    sw $t0, carLane
   
    # Cargar posici?n correcta del carril centro
    lw $s1, lane1X
    sw $s1, carX          # Actualizar carX tambi?n
   
    lw $s2, carY
    # Inicializar primer obst?culo en carril centro (centrado)
    lw $s3, obsLane1X
    sw $s3, obsX
    lw $s4, obsY
    lw $s5, carSpeed

loop:
    # Actualizar contador de frames para mejor aleatoriedad
    lw $t9, frameCounter
    addi $t9, $t9, 1
    sw $t9, frameCounter
   
    # Si randomSeed está sin inicializar (0), inicializa con frameCounter
    lw $t9, randomSeed
    bne $t9, 0, skipSeedInit
    lw $t9, frameCounter
    sw $t9, randomSeed
skipSeedInit:
    # === VERIFICAR SLOWDOWN ACTIVO ===
    lw $t9, slowdownActive
    beqz $t9, continueNormal
    
    # Si hay slowdown, mantener velocidad máxima en 0
    lw $t8, carSpeed
    blez $t8, continueNormal
    li $t8, 0
    sw $t8, carSpeed
    move $s5, $t8
    
continueNormal:
    # === FASE 1: CALCULAR (sin dibujar nada) ===
    # Mover obstáculo considerando slowdown
    lw $t8, carSpeed
    
normalSpeed:
    lw $t9, slowdownActive
    beqz $t9, addNormalSpeed
    
    # Con slowdown, el mundo se mueve más rápido (efecto relativo)
    addi $t8, $t8, 3
    j moveObstacle
    
addNormalSpeed:
    addi $t8, $t8, 2
    
moveObstacle:
    add $s4, $s4, $t8
    sw $s4, obsY
   
    # Reset obstáculo
    li $t0, 128
    blt $s4, $t0, skipReset
    
    # Reset obst?culo
    li $t0, 128
    blt $s4, $t0, skipReset
   
    li $s4, -8
    sw $s4, obsY
   
    lw $t0, score
    addi $t0, $t0, 1
    sw $t0, score
   
    # Mezclar semilla con frameCounter y posici?n Y para m?s aleatoriedad
    lw $t8, randomSeed
    lw $t9, frameCounter
    xor $t8, $t8, $t9
    lw $t7, obsY
    add $t8, $t8, $t7
    sw $t8, randomSeed

    # Elegir tipo aleatorio (0 = cono, 1 = piedra)
    li $a0, 4
    jal getRandom
    sw $v0, obsType

    # Elegir carril aleatorio (0 = izq, 1 = centro, 2 = derecha)
    lw $t8, randomSeed
    lw $t9, frameCounter
    sll $t9, $t9, 3          # Mezcla extra
    xor $t8, $t8, $t9
    sw $t8, randomSeed
    li $a0, 3
    jal getRandom
    move $t1, $v0

    beq $t1, 0, setXFarLeft
    beq $t1, 1, setXLeft
    beq $t1, 2, setXRight
   
    # Elegir tipo aleatorio de obst?culo (0 = cono, 1 = piedra)
    li $a0, 4
    jal getRandom
    sw $v0, obsType

    # Elegir carril aleatorio (0 = izq, 1 = centro, 2 = derecha)
    li $a0, 3
    jal getRandom
    move $t1, $v0

    beq $t1, 0, setXFarLeft
    beq $t1, 1, setXLeft
    beq $t1, 2, setXRight
    li $s3, 43
    sw $s3, obsX
    j skipReset

setXFarLeft:
    lw $s3, obsLane0X
    sw $s3, obsX
    j skipReset
   
setXLeft:
    lw $s3, obsLane1X
    sw $s3, obsX
    j skipReset
   
setXRight:
    lw $s3, obsLane2X
    sw $s3, obsX

skipReset:
    # === FASE 2: DIBUJAR TODO DE UN TIR?N ===
    jal updateScroll
    jal drawTrack         # Dibuja fondo
   
    # Dibujar obst?culo PRIMERO
    move $a0, $s3
    move $a1, $s4
    jal drawObstacle
   
    # Dibujar auto DESPU?S (queda arriba)
    move $a0, $s1
    move $a1, $s2
    jal drawCarSprite32
   
    # Dibujar HUD
    jal drawHearts
    jal drawScore
   
    # === FASE 3: INPUT ===
    li $t0, 0xffff0000
    lw $t1, 0($t0)
    andi $t1, $t1, 1
    beq $t1, 0, noKey
   
    lw $t1, 4($t0)
    beq $t1, 'a', moveLeft
    beq $t1, 'A', moveLeft
    beq $t1, 'd', moveRight
    beq $t1, 'D', moveRight
    beq $t1, 'w', moveUp
    beq $t1, 'W', moveUp
    beq $t1, 's', moveDown
    beq $t1, 'S', moveDown
    beq $t1, 'q', exit
    beq $t1, 'Q', exit
    j noKey
   
# (moveLeft, moveRight, etc. IGUAL que antes)
moveLeft:
    lw $t2, carLane
    beqz $t2, noKey
    addi $t2, $t2, -1
    sw $t2, carLane
   
    beq $t2, 0, setLane0
    beq $t2, 1, setLane1
    j noKey

setLane0:
    lw $t3, lane0X
    sw $t3, carX
    move $s1, $t3
    j noKey
   
setLane1:
    lw $t3, lane1X
    sw $t3, carX
    move $s1, $t3
    j noKey
   
moveRight:
    lw $t2, carLane
    li $t3, 2
    bge $t2, $t3, noKey
    addi $t2, $t2, 1
    sw $t2, carLane
   
    beq $t2, 1, setLane1Right
    beq $t2, 2, setLane2
    j noKey

setLane1Right:
    lw $t3, lane1X
    sw $t3, carX
    move $s1, $t3
    j noKey
   
setLane2:
    lw $t3, lane2X
    sw $t3, carX
    move $s1, $t3
    j noKey

moveUp:
    lw $t2, carSpeed
    li $t3, 3
    bge $t2, $t3, noKey
    
    # Verificar si hay slowdown activo
    lw $t4, slowdownActive
    beqz $t4, normalMoveUp
    
    # Con slowdown, solo puede acelerar hasta velocidad 1
    li $t3, 1
    bge $t2, $t3, noKey
    
normalMoveUp:
    addi $t2, $t2, 1
    sw $t2, carSpeed
    move $s5, $t2
    j noKey

moveDown:
    lw $t2, carSpeed
    blez $t2, noKey
    addi $t2, $t2, -1
    sw $t2, carSpeed
    move $s5, $t2
    j noKey

noKey:
    # === FASE 4: SLOWDOWN Y COLISIONES ===
    # Actualizar timer del slowdown
    lw $t0, slowdownActive
    beqz $t0, checkCollision
    
    lw $t1, slowdownTimer
    addi $t1, $t1, -1
    sw $t1, slowdownTimer
    
    bgtz $t1, checkCollision
    # Timer llegó a 0, desactivar slowdown
    sw $zero, slowdownActive

checkCollision:
    bltz $s4, noCol
    li $t0, 128
    bge $s4, $t0, noCol
   
    move $t0, $s1
    addi $t0, $t0, -10
    move $t1, $s1
    addi $t1, $t1, 10
    move $t2, $s2
    addi $t2, $t2, 5
    move $t3, $s2
    addi $t3, $t3, 27
   
    move $t4, $s3
    addi $t4, $t4, 2
    move $t5, $s3
    addi $t5, $t5, 14
    move $t6, $s4
    addi $t6, $t6, 2
    move $t7, $s4
    addi $t7, $t7, 14
   
    bge $t0, $t5, noCol
    ble $t1, $t4, noCol
    bge $t2, $t7, noCol
    ble $t3, $t6, noCol
   
    # COLISIÓN DETECTADA - verificar tipo
    lw $t0, obsType
    beq $t0, 3, charcoCollision  # Si es charco (tipo 3), manejar diferente
    j normalCollision

charcoCollision:
    # Activar slowdown por 3 segundos
    li $t0, 1
    sw $t0, slowdownActive
    li $t0, 30           # ~3 segundos a 50 frames/seg
    sw $t0, slowdownTimer
    
    # FORZAR velocidad del auto a 0 (frenar)
    li $t0, 0
    sw $t0, carSpeed
    move $s5, $t0
    
    # Resetear obstáculo
    li $s4, -20
    sw $s4, obsY
    
    # Generar tipo aleatorio: 0, 1, 2 o 3
    li $a0, 4
    jal getRandom
    sw $v0, obsType
   
    # Generar carril aleatorio: 0, 1 o 2
    li $a0, 3
    jal getRandom
    move $t1, $v0
   
    beq $t1, 0, charcoSetXFarLeft
    beq $t1, 1, charcoSetXLeft
    beq $t1, 2, charcoSetXRight
    j noCol

charcoSetXFarLeft:
    lw $s3, obsLane0X
    sw $s3, obsX
    j noCol
   
charcoSetXLeft:
    lw $s3, obsLane1X
    sw $s3, obsX
    j noCol
   
charcoSetXRight:
    lw $s3, obsLane2X
    sw $s3, obsX
    j noCol

normalCollision:
    lw $t0, lives
    addi $t0, $t0, -1
    sw $t0, lives
   
    li $v0, 4
    la $a0, hitMsg
    syscall
   
    li $v0, 1
    move $a0, $t0
    syscall
   
    li $v0, 4
    la $a0, newlineMsg
    syscall
   
    beqz $t0, exit
   
    li $s4, -20
    sw $s4, obsY
   
    # Generar tipo aleatorio: 0, 1, 2 o 3
    li $a0, 4
    jal getRandom
    sw $v0, obsType
   
    # Generar carril aleatorio: 0, 1 o 2
    li $a0, 3
    jal getRandom
    move $t1, $v0
   
    beq $t1, 0, collSetXFarLeft
    beq $t1, 1, collSetXLeft
    beq $t1, 2, collSetXRight
    j noCol

collSetXFarLeft:
    lw $s3, obsLane0X
    sw $s3, obsX
    j noCol
   
collSetXLeft:
    lw $s3, obsLane1X
    sw $s3, obsX
    j noCol
   
collSetXRight:
    lw $s3, obsLane2X
    sw $s3, obsX
    j noCol

noCol:
    # Delay más agresivo
    li $t0, 50000
delayLoop:
    addi $t0, $t0, -1
    bgtz $t0, delayLoop
   
    j loop
   
    j loop

exit:
    li $v0, 4
    la $a0, exitMsg
    syscall
    li $v0, 10
    syscall

fillScreen:
    move $t0, $a0
    move $t1, $a1
    li $t2, 8192
fillLoop:
    sw $t1, 0($t0)
    addi $t0, $t0, 4
    addi $t2, $t2, -1
    bgtz $t2, fillLoop
    jr $ra

clearScreen:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
   
    li $a0, 0x10008000
    li $a1, 0x000000        # Negro
    jal fillScreen
   
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra

updateScroll:
    lw $t0, scrollOffset
    lw $t1, carSpeed
   
    # Scroll m?s suave
    addi $t0, $t0, 1        # Era 2, ahora 1 (m?s suave)
   
    # Agregar velocidad del auto
    add $t0, $t0, $t1
   
    # Loop cuando llega a 64
    li $t2, 64
loopCheck:
    blt $t0, $t2, saveScroll
    sub $t0, $t0, $t2
    j loopCheck
   
saveScroll:
    sw $t0, scrollOffset
    jr $ra


# REEMPLAZA COMPLETAMENTE drawTrack con esta versi?n:
drawTrack:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
   
    lw $t9, scrollOffset    # Cargar offset actual
   
    # Sprite 1: Arriba (puede estar parcialmente fuera)
    li $a0, 0               # X = 0
    move $a1, $t9           # Y = offset
    addi $a1, $a1, -64      # Restar 64 para que empiece arriba
    jal drawPista64Simple
   
    # Sprite 2: Centro
    li $a0, 0
    move $a1, $t9           # Y = offset
    jal drawPista64Simple
   
    # Sprite 3: Abajo
    li $a0, 0
    move $a1, $t9
    addi $a1, $a1, 64       # Sumar 64 para que est? abajo
    jal drawPista64Simple
   
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra


drawPista64Simple:
    addi $sp, $sp, -24
    sw $ra, 0($sp)
    sw $s0, 4($sp)
    sw $s1, 8($sp)
    sw $s2, 12($sp)
    sw $s3, 16($sp)
    sw $s4, 20($sp)
   
    move $s0, $a0           # X inicial
    move $s1, $a1           # Y inicial
   
    la $s2, pista64_data
    li $s3, 0               # fila

rowLoop:
    add $s4, $s1, $s3       # Y_pantalla
   
    # Clipping vertical
    bltz $s4, skipRow
    li $t0, 128
    bge $s4, $t0, endSprite
   
    # Calcular direcci?n base de la fila UNA SOLA VEZ
    li $t2, 0x10008000
    move $t3, $s4
    sll $t3, $t3, 6         # Y * 64 (optimizado)
    add $t3, $t3, $s0
    sll $t3, $t3, 2
    add $t2, $t2, $t3       # Direcci?n base de la fila
   
    li $s5, 0               # columna

colLoop:
    lw $t0, 0($s2)          # RGBA
    srl $t1, $t0, 24        # Alpha
   
    # SI alpha = 0, dibujar NEGRO (esto borra el fondo)
    beqz $t1, drawBlack
    andi $t0, $t0, 0x00FFFFFF   # RGB
    j drawIt
   
drawBlack:
    li $t0, 0x000000        # Negro
   
drawIt:
    # Dibujar directamente
    sw $t0, 0($t2)
    addi $t2, $t2, 4        # Siguiente p?xel

    addi $s2, $s2, 4
    addi $s5, $s5, 1
    li $t0, 64
    blt $s5, $t0, colLoop
    j nextRow

skipRow:
    addi $s2, $s2, 256      # Saltar fila

nextRow:
    addi $s3, $s3, 1
    li $t0, 64
    blt $s3, $t0, rowLoop

endSprite:
    lw $ra, 0($sp)
    lw $s0, 4($sp)
    lw $s1, 8($sp)
    lw $s2, 12($sp)
    lw $s3, 16($sp)
    lw $s4, 20($sp)
    addi $sp, $sp, 24
    jr $ra
   
# Dibujar auto ROJO completo (basado en tu sprite)
# $a0 = x (centro), $a1 = y (arriba)
